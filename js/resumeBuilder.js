projects.display = function() {
    var formatProjects = [
        [HTMLprojectTitle, "title"],
        [HTMLprojectDates, "dates"],
        [HTMLprojectDescription, "description"],
    ];
    $.each(projects.projects, function(k, v) {
        var formattedProjects = '';
        $('#projects').append(HTMLprojectStart);
        $.each(formatProjects, function(j, x) {
            formattedProjects += formatProjects[j][0].replace('%data%', projects.projects[k][formatProjects[j][1]]);
        });
        $.each(projects.projects[k]["images"], function(l, y) {
            formattedProjects += HTMLprojectImage.replace('%data%', projects.projects[k]["images"][l]);
        });
        formattedProjects = formattedProjects.replace('#', projects.projects[k].url);
        $('.project-entry').last().append(formattedProjects);
    });
}();

